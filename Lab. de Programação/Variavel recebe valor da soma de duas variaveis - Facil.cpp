#include <stdio.h>
#include <iostream>
 
int main() {
    
    int A, B, X;
    
    scanf("%d", &A);
    
    scanf("%d", &B);
    
    X = A + B; // X recebe o valor resultante da soma de A + B
    
    printf("X = %d\n", X); // Posição do %d tem que estar onde o valor da variavel tem que aparecer.

    return 0;
}
